function initGettingReady() {
  showGettingReadyModules();
}

function showGettingReadyModules() {
  document.getElementById('getting-ready-content').style.display = 'none';
  document.querySelectorAll('#page-getting-ready > .grid').forEach(g => g.style.display = 'grid');
}

function showGettingReadySection(sectionId) {
  const section = GETTING_READY_SECTIONS[sectionId];
  if (!section) return;

  // Hide module cards, show content
  document.querySelectorAll('#page-getting-ready > .grid').forEach(g => g.style.display = 'none');
  document.getElementById('getting-ready-content').style.display = 'block';

  let html = '<h2>' + section.title + '</h2>';
  html += section.content;

  document.getElementById('getting-ready-content-inner').innerHTML = html;

  // Track viewed sections
  if (!currentUser.progress.gettingReadyViewed) currentUser.progress.gettingReadyViewed = [];
  if (!currentUser.progress.gettingReadyViewed.includes(sectionId)) {
    currentUser.progress.gettingReadyViewed.push(sectionId);
    saveUser();
  }
}

// Map each module emoji to a line-icon (all tinted blue, the Mindset pillar colour)
var JJ_MIND_ICONS = { '🧠':'pulse', '💪':'shield', '🎯':'target', '☀️':'sun', '😴':'moon', '🌅':'sunrise', '🎬':'film', '🌄':'mountain' };
function jjMindIcon(e){ return JJ_MIND_ICONS[e] || 'mindset'; }
function jjHydrateEl(el){ if (window.JJIcons && el) { JJIcons.hydrate(el); } }

function getTodaysMindsetExercise() {
  // Use the day of the year to cycle through exercises
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((now - start) / (1000 * 60 * 60 * 24));
  return DAILY_MINDSET_EXERCISES[dayOfYear % DAILY_MINDSET_EXERCISES.length];
}

function isMindsetExerciseDoneToday() {
  if (!currentUser || !currentUser.progress.mindsetExerciseLog) return false;
  const today = new Date().toISOString().split('T')[0];
  return currentUser.progress.mindsetExerciseLog.includes(today);
}

function completeMindsetExercise() {
  if (!currentUser.progress.mindsetExerciseLog) currentUser.progress.mindsetExerciseLog = [];
  const today = new Date().toISOString().split('T')[0];
  if (!currentUser.progress.mindsetExerciseLog.includes(today)) {
    currentUser.progress.mindsetExerciseLog.push(today);
    saveUser();
  }
  showToast('Daily exercise done! 👍');
  renderDailyMindsetExercise();
}

function renderDailyMindsetExercise() {
  const container = document.getElementById('daily-mindset-exercise');
  if (!container) return;
  
  const exercise = getTodaysMindsetExercise();
  const done = isMindsetExerciseDoneToday();
  
  let html = '<div class="flex items-center gap-md mb-md">';
  html += '<span class="jj-icon icon-emoji-lg" data-jjicon="mindset" data-jjcolor="blue"></span>';
  html += '<div><h3 class="mb-0">Today\'s Exercise: ' + exercise.title + '</h3>';
  html += '<span class="form-hint">' + exercise.duration + ' · Changes daily</span></div>';
  html += '</div>';
  html += '<p style="font-size: var(--font-size-base); color: var(--text-secondary); line-height: 1.7;">' + exercise.instruction + '</p>';
  
  if (done) {
    html += '<div class="alert alert-success mt-md" style="margin-bottom:0;">✅ Done for today. Nice one. Come back tomorrow for a new one.</div>';
  } else {
    html += '<button class="btn btn-primary mt-md" onclick="completeMindsetExercise()">✅ Done It</button>';
  }
  
  container.innerHTML = html; jjHydrateEl(container);
}

function initMindset() {
  renderDailyMindsetExercise();
  showMindsetModules();
}

function showMindsetModules() {
  document.getElementById('mindset-content').style.display = 'none';
  document.getElementById('mindset-modules').style.display = 'grid';
  
  const container = document.getElementById('mindset-modules');
  const completed = currentUser.progress.mindsetCompleted || [];
  
  let html = '';
  MINDSET_MODULES.forEach(mod => {
    const done = completed.includes(mod.id);
    html += '<div class="mindset-module-card ' + (done ? 'completed' : '') + '" onclick="openMindsetModule(\'' + mod.id + '\')">';
    html += '<span class="module-status">' + (done ? '✅' : '📖') + '</span>';
    html += '<span class="jj-icon icon-emoji-lg" data-jjicon="' + jjMindIcon(mod.emoji) + '" data-jjcolor="blue"></span>';
    html += '<h3>' + mod.title + '</h3>';
    html += '<p>' + mod.subtitle + '</p>';
    html += '</div>';
  });
  
  container.innerHTML = html; jjHydrateEl(container);
}

function openMindsetModule(id) {
  const mod = MINDSET_MODULES.find(m => m.id === id);
  if (!mod) return;
  
  document.getElementById('mindset-modules').style.display = 'none';
  document.getElementById('mindset-content').style.display = 'block';
  
  let html = '<h2><span class="jj-icon" data-jjicon="' + jjMindIcon(mod.emoji) + '" data-jjcolor="blue"></span> ' + mod.title + '</h2>';
  html += mod.content;
  html += '<div style="margin-top:var(--space-2xl); text-align:center;">';
  
  const done = (currentUser.progress.mindsetCompleted || []).includes(id);
  if (!done) {
    html += '<button class="btn btn-primary btn-lg" onclick="completeMindsetModule(\'' + id + '\')">✅ Mark as Read</button>';
  } else {
    html += '<div class="alert alert-success">You\'ve completed this module! ✅</div>';
  }
  html += '</div>';
  
  document.getElementById('mindset-content-inner').innerHTML = html; jjHydrateEl(document.getElementById('mindset-content-inner'));
  
  // Mark as viewed
  if (!currentUser.progress.mindsetCompleted) currentUser.progress.mindsetCompleted = [];
}

function completeMindsetModule(id) {
  if (!currentUser.progress.mindsetCompleted.includes(id)) {
    currentUser.progress.mindsetCompleted.push(id);
    saveUser();
    showToast('Module completed! 🌟');
  }
  showMindsetModules();
}
